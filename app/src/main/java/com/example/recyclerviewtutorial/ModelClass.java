package com.example.recyclerviewtutorial;

public class ModelClass {

   public class resultdata{
       String id;
       String title;
       String image;
       String web_image;
       String description;
       String order_sequence;
       String status;
       String created_on;
       String charges;
       String status_type;

       public String getId() {
           return id;
       }

       public void setId(String id) {
           this.id = id;
       }

       public String getTitle() {
           return title;
       }

       public void setTitle(String title) {
           this.title = title;
       }

       public String getImage() {
           return image;
       }

       public void setImage(String image) {
           this.image = image;
       }

       public String getWeb_image() {
           return web_image;
       }

       public void setWeb_image(String web_image) {
           this.web_image = web_image;
       }

       public String getDescription() {
           return description;
       }

       public void setDescription(String description) {
           this.description = description;
       }

       public String getOrder_sequence() {
           return order_sequence;
       }

       public void setOrder_sequence(String order_sequence) {
           this.order_sequence = order_sequence;
       }

       public String getStatus() {
           return status;
       }

       public void setStatus(String status) {
           this.status = status;
       }

       public String getCreated_on() {
           return created_on;
       }

       public void setCreated_on(String created_on) {
           this.created_on = created_on;
       }

       public String getCharges() {
           return charges;
       }

       public void setCharges(String charges) {
           this.charges = charges;
       }

       public String getStatus_type() {
           return status_type;
       }

       public void setStatus_type(String status_type) {
           this.status_type = status_type;
       }
   }
}
